    DECLARE v_date_commande DATE;
    DECLARE v_nb_exemplaires_a_creer INT;
    DECLARE v_next_numero INT;
    DECLARE v_counter INT DEFAULT 0;
    DECLARE v_id_etat_neuf CHAR(5) DEFAULT '00001'; -- ID de l'état 'neuf'
    DECLARE v_id_suivi_livree INT DEFAULT 2;      -- ID du suivi 'livrée'

    -- Se déclenche uniquement si l'étape passe à 'livrée' (et n'était pas déjà 'livrée')
    IF NEW.idSuivi = v_id_suivi_livree AND OLD.idSuivi <> v_id_suivi_livree THEN

        -- Récupérer la date de commande correspondante
        SELECT dateCommande INTO v_date_commande
        FROM commande
        WHERE id = NEW.id; -- Utilise l'ID de la ligne mise à jour (qui est le même que l'ID commande)

        -- Récupérer le nombre d'exemplaires à créer
        SET v_nb_exemplaires_a_creer = NEW.nbExemplaire;

        -- Trouver le prochain numéro d'exemplaire séquentiel pour ce document
        SELECT IFNULL(MAX(numero), 0) + 1 INTO v_next_numero
        FROM exemplaire
        WHERE id = NEW.idLivreDvd;

        -- Boucle pour insérer les exemplaires
        WHILE v_counter < v_nb_exemplaires_a_creer DO
            INSERT INTO exemplaire (id, numero, dateAchat, photo, idEtat)
            VALUES (NEW.idLivreDvd, v_next_numero, v_date_commande, '', v_id_etat_neuf);

            -- Incrémenter les compteurs pour le prochain exemplaire
            SET v_next_numero = v_next_numero + 1;
            SET v_counter = v_counter + 1;
        END WHILE;

    END IF;
END
